#Manipulating variables
# we can always change the assigned value of a variable by overriding the previous value.
a=2*3
print(a)
a=9*2
print(a)
# in this condition initially the output will be 6 which is over ridden by assigning the same variable a different value. Therefore the output now will be 18
# variable defined in a code cell----> all code cells have access to the variable
# the variable can be used in operations too
